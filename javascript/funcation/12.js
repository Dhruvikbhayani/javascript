var abc=function (x){

    var a=0
    var b=x
    for(i=1;i<x;i++){
        if(x%i==0){
            Math.round(x/2)
        a+=x
        }
    }
    if(a==b){
        console.log("This is perfect numbers")
    }
    else{
        console.log("this is not perfect number")
    }
}
abc(7)